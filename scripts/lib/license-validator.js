// license-validator.js — entry-point gate pentru robOS.
//
// Combina:
//   1. Cross-hash integrity check (acest fisier + 5 peers critici)
//   2. License check delegat la scripts/license-check.js
//
// Apelat din 4 entry points: hook-user-prompt.js, centre/server.js,
// scripts/setup.js, scripts/update.js. Toate 4 trebuie modificate in
// lockstep ca bypass-ul sa fie complet — plus hash-urile trebuie refresh-uite
// la fiecare modificare (vezi scripts/rehash-validators.js).
//
// Tampering protection:
//   - SELF_HASH detecteaza modificare la acest fisier.
//   - PEER hashes detecteaza modificare la oricare din cele 5 fisiere
//     critice. Liniile cu marker "robos:i" sunt strippate inainte de hash,
//     ca tabela de hash-uri sa nu auto-invalideze.
//
// Mesajele de eroare raman student-friendly (vezi LIC-5 in INVARIANTS.md).

import { existsSync, readFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { checkLicense } from '../license-check.js';

const __filename_lv = fileURLToPath(import.meta.url);
const ROBOS_ROOT = join(dirname(__filename_lv), '..', '..');

// === BEGIN INTEGRITY MANIFEST === (regenerated by scripts/rehash-validators.js)
// DO NOT EDIT HASHES BY HAND. Run: node scripts/rehash-validators.js
const MANIFEST = {                                                                          // robos:i
  self: 'b61eb14e7109a8eb8e79088c0dedf9cc81385609fa8467cd40b9f098ea76589d',                                                              // robos:i
  peers: {                                                                                  // robos:i
    'scripts/license-check.js': 'b56f8905c04be618544f8720c03809cf3268062f29cf8fbfa54925b7e313abed',                                        // robos:i
    'scripts/hook-user-prompt.js': '61a25cd44bc7ea00098d20b51e4b78c64df16fe802ed9711ec14dd22562b9955',                                   // robos:i
    'centre/server.js': '534cb8da3865ad8da1092924b2413180f5039a372b0b0a161230dff30fac9487',                                            // robos:i
    'scripts/setup.js': 'fb7c4245349d4ca436c9ed8bfcffdc5949f6992ad7f573c7b0e0e4dabadb8dc9',                                             // robos:i
    'scripts/update.js': 'be215cb3aad7b2dccb11ef6f29dcff10b1bd9bc834bd735444eae8c7e0dde563',                                           // robos:i
  },                                                                                        // robos:i
};                                                                                          // robos:i
// === END INTEGRITY MANIFEST ===

const MARKER = 'robos:i';

function hashContent(content) {
  const stripped = content
    .split('\n')
    .filter((l) => !l.includes(MARKER))
    .join('\n');
  return createHash('sha256').update(stripped).digest('hex');
}

function hashFile(absPath) {
  if (!existsSync(absPath)) return null;
  return hashContent(readFileSync(absPath, 'utf-8'));
}

export function verifyIntegrity() {
  // Self
  const selfActual = hashContent(readFileSync(__filename_lv, 'utf-8'));
  if (selfActual !== MANIFEST.self) {
    return { ok: false, file: 'scripts/lib/license-validator.js' };
  }
  // Peers
  for (const [rel, expected] of Object.entries(MANIFEST.peers)) {
    const abs = join(ROBOS_ROOT, rel);
    const actual = hashFile(abs);
    if (actual === null) continue; // peer optional (instalare minimala)
    if (actual !== expected) {
      return { ok: false, file: rel };
    }
  }
  return { ok: true };
}

export async function checkLicenseAndIntegrity(rootDir) {
  const integrity = verifyIntegrity();
  if (!integrity.ok) {
    return {
      ok: false,
      code: 'integrity_fail',
      message: `Integritate robOS compromisa (${integrity.file}). Reinstaleaza arhiva originala din emailul de download sau scrie-mi: adrian@robos.vip.`,
      action: 'reinstall',
    };
  }
  return checkLicense(rootDir || ROBOS_ROOT);
}

// CLI: `node scripts/lib/license-validator.js` printeaza statusul integritatii + licenta
const __invoked_as_main = process.argv[1] && process.argv[1].replace(/\\/g, '/') === __filename_lv.replace(/\\/g, '/');
if (__invoked_as_main) {
  checkLicenseAndIntegrity(ROBOS_ROOT).then((r) => {
    console.log(JSON.stringify(r, null, 2));
    process.exit(r.ok ? 0 : 1);
  });
}
